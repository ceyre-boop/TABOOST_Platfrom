// Calculate growth percentage
function getGrowth(creator) {
    if (!creator.diamondsLast || creator.diamondsLast === 0) return 0;
    return ((creator.diamonds - creator.diamondsLast) / creator.diamondsLast * 100);
}

// Get top gainers (most improved)
function getTopGainers(limit = 10) {
    return ALL_CREATORS.map(c => ({...c, growth: getGrowth(c)}))
        .filter(c => c.growth > 0 && c.diamondsLast > 0)
        .sort((a, b) => b.growth - a.growth)
        .slice(0, limit);
}

// Get all creators for table
function getAllCreators() {
    return ALL_CREATORS;
}

// Get creators by filter
function filterCreators(group, manager, status) {
    return ALL_CREATORS.filter(c => {
        if (group && c.group !== group) return false;
        if (manager && !c.manager.includes(manager)) return false;
        if (status && !c.status.includes(status)) return false;
        return true;
    });
}

// Calculate total stats
function getTotalStats() {
    return {
        totalCreators: ALL_CREATORS.length,
        totalDiamonds: ALL_CREATORS.reduce((sum, c) => sum + c.diamonds, 0),
        totalHours: ALL_CREATORS.reduce((sum, c) => sum + c.hours, 0),
        totalStreams: ALL_CREATORS.reduce((sum, c) => sum + c.streams, 0)
    };
}

// Get streaming hours distribution
function getHoursDistribution() {
    const tiers = {
        '0h': { min: 0, max: 0, count: 0, creators: [] },
        '1-10h': { min: 1, max: 10, count: 0, creators: [] },
        '11-25h': { min: 11, max: 25, count: 0, creators: [] },
        '26-50h': { min: 26, max: 50, count: 0, creators: [] },
        '51-100h': { min: 51, max: 100, count: 0, creators: [] },
        '100h+': { min: 101, max: Infinity, count: 0, creators: [] }
    };
    
    ALL_CREATORS.forEach(c => {
        const h = c.hours;
        if (h === 0) {
            tiers['0h'].count++;
            if (tiers['0h'].creators.length < 3) tiers['0h'].creators.push(c.username);
        } else if (h <= 10) {
            tiers['1-10h'].count++;
            if (tiers['1-10h'].creators.length < 3) tiers['1-10h'].creators.push(c.username);
        } else if (h <= 25) {
            tiers['11-25h'].count++;
            if (tiers['11-25h'].creators.length < 3) tiers['11-25h'].creators.push(c.username);
        } else if (h <= 50) {
            tiers['26-50h'].count++;
            if (tiers['26-50h'].creators.length < 3) tiers['26-50h'].creators.push(c.username);
        } else if (h <= 100) {
            tiers['51-100h'].count++;
            if (tiers['51-100h'].creators.length < 3) tiers['51-100h'].creators.push(c.username);
        } else {
            tiers['100h+'].count++;
            if (tiers['100h+'].creators.length < 3) tiers['100h+'].creators.push(c.username);
        }
    });
    
    return tiers;
}

// Format numbers
function formatNumber(num) {
    if (num >= 1000000) return (num / 1000000).toFixed(1) + 'M';
    if (num >= 1000) return (num / 1000).toFixed(1) + 'K';
    return num.toString();
}

